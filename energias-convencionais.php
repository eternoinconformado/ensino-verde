                        <li>Hidrofluorcarbono (HFC)</li>
                        <li>Perfluorcarbono (PFC)</li>
                    </ul>

                    <p>Logo, nos anos seguintes o principal trabalho desses países é equilibrar cada vez mais o uso dessas energias para preservar o nosso planeta.</p>

                    <h2>Quais os dois tipos de fonte de energia?</h2>
                    <p>Os dois tipos principais de fonte de energia são:</p>

                    <ol>
                        <li>Renováveis</li>
                        <li>Não renováveis</li>
                    </ol>

                    <p>As renováveis, é o tipo de energia que vem de recursos que não se esgotam, se renovando rapidamente e não geram resíduos poluentes, exemplos desse tipo de energia são:</p>
                    <ul>
                        <li>energia eólica</li>
                        <li>energia solar</li>
                        <li>energia de biomassa</li>
                        <li>energia geotérmica</li>
                        <li>energia maremotriz</li>
                    </ul>

                    <p>Já as não renováveis, vem de combustíveis fósseis e da energia nuclear, além de ser mais poluentes.</p>

                    <p>No Brasil, de acordo com o balanço energético nacional (BEN) divulgado pela empresa de pesquisa elétrica (EPE), 83% da nossa matriz energética é baseada em recursos renováveis, ficando acima dos 25% da média mundial.</p>

                    <h2>Exemplos de energia convencional</h2>

                    <h3>Petróleo</h3>

                    <p>É o principal combustível fóssil utilizado atualmente. Sua extração é feita em áreas de bacia sedimentar que concentram grande quantidade de matéria orgânica e sedimentos de rochas solidificadas a milhões de anos. Podendo ser formada tanto em terra quanto no mar.</p>
                    
                    <p>O pré-sal brasileiro é um exemplo de petróleo localizado no mar, e em terra o oriente médio tem os maiores produtores mundiais: Iraque, Kuwait e Emirados Árabes Unidos.</p>

                    <p>Do petróleo derivam vários de subprodutos como gasolina, óleo diesel, asfalto, gás de cozinha, dentre outros.</p>

                    <h3>Carvão</h3>

                    <p>Usado desde a revolução industrial, ele é uma das fontes de energia não renovável. Também é extraído de bacias sedimentares e é formado por restos de vegetais sedimentados a milhões de anos.</p>

                    <p>Quanto maior o teor de carbono na matéria, maior o tempo de sedimentação. Dessa forma, o carvão é classificado em quatro tipos:</p>

                    <ol>
                        <li>Antracito: é o mais antigo, porém raro. Pode conter até 96% de teor de carbono;</li>
                        <li>Hulha: é o mais encontrado e utilizado atualmente com teor de carbono entre 80% e 90%.</li>
                        <li>Linhita: possui teor de carbono de até 75%;</li>
                        <li>Turfa: rocha mais “jovem” com teor de carbono até 60%.</li>
                    </ol>

                    <p>Usado em usinas termelétricas para gerar energia e também para produção de plásticos, tintas, fertilizantes, entre outros.</p>

                    <h3>Gás natural</h3>
                    
                    <p>Composto por hidrocarbonetos e metano, o gás natural é originado pela decomposição da matéria orgânica fossilizada. </p>

                    <p>Pois junto com o petróleo, outra fonte de energia que pode ser extraída é o gás natural.</p>

                    <p>É utilizado em termelétricas, mas além disso, pode ser usado nos sistemas de aquecimento de água nas residências e nos sistemas de aquecimento em países de clima frio.</p>

                    <p>Temos também a energia nuclear que é classificada como convencional.</p>

                    <h2>Vantagens e desvantagens</h2>

                    <p>Pelo que já foi pontuado, podemos perceber que os países ainda são bastante dependentes das energias convencionais, isso porque ela traz algumas vantagens:</p>

                    <ul>
                        <li>facilidade de extrair e processar as matérias-primas;</li>
                        <li>descoberta de reservas facilitada pela tecnologia;</li>
                        <li>formas de transporte fáceis e acessíveis;</li>
                        <li>custo baixo para operação;</li>
                        <li>taxa de eficiência energética alta.</li>
                    </ul>

                    <p>Porém, temos também as desvantagens que trazem impactos globais:</p>

                    <ul>
                        <li>geração de resíduos alta, em especial, de gases estufa;</li>
                        <li>contaminação do ar e da água;</li>
                        <li>preço de aquisição muito alto;</li>
                        <li>aquecimento global que impacta toda a vida no planeta.</li>
                    </ul>

                    <p>Por isso, cada vez mais os países estão buscando fontes alternativas que cause menos impactos negativos ao nosso planeta, como as energias renováveis, que estão se tornando mais presentes na matriz energética.</p>

