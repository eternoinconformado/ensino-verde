									<p>As temperaturas mundiais estão subindo por causa da atividade humana, e as mudanças climáticas agora ameaçam todos os aspectos da vida humana. Da elevação dos mares ao aquecimento das temperaturas e furacões e tornados mais violentos, os impactos das mudanças climáticas estão piorando. Em todos os continentes, a vida como a conhecemos está mudando devido ao aumento das temperaturas - e não para melhor. Se a situação não for controlada, os humanos e a natureza passarão por um aquecimento catastrófico, com o agravamento das secas, maior aumento do nível do mar e extinção em massa de espécies.</p>

									<hr class="major" />

									<h2>O que é mudança climática?</h2>
									<p>A mudança climática é uma mudança nas condições médias de temperatura e ambiente num lugar. A rápida mudança climática que estamos vendo agora é causada pelo uso humano de petróleo, gás e carvão para casas, fábricas e transporte. O planeta está agora cerca de 1,2°C mais quente do que no século 19 — e a quantidade de CO2 na atmosfera aumentou em 50%. Se nada for feito, os cientistas acreditam que o aquecimento global pode ultrapassar os 4ºC até o ano 2100, levando a ondas de calor devastadoras, milhões perdendo suas casas devido à elevação do nível do mar e perda irreversível de espécies vegetais e animais.</p>
									
									<p>A questão é tão urgente que o Dicionário Oxford elegeu "emergência climática" – uma situação em que é necessária uma ação urgente para reduzir ou interromper a mudança climática e evitar danos ambientais potencialmente irreversíveis – a “palavra do ano” de 2019. Segundo o dicionário britânico, o uso do termo aumentou mais de 100 vezes desde setembro de 2018.</p>
									
									<hr class="major" />

									<h2>Como as diferentes partes do mundo serão afetadas?</h2>
									<p>As mudanças climáticas têm efeitos distintos em diferentes áreas do mundo. Alguns lugares aquecerão mais do que outros, alguns receberão mais chuvas – causando inundações históricas, como visto recentemente na China, Alemanha, Bélgica e Holanda – e outros enfrentarão mais secas.</p>

									<p>Se o aumento da temperatura não puder ser mantido dentro de 1,5°C:</p>
									
									<ul>
										<li>O Reino Unido e a Europa serão vulneráveis a inundações causadas por chuvas extremas;</li>
										<li>Os países do Oriente Médio passarão por ondas de calor extremas e as terras agrícolas podem se transformar em deserto;</li>
										<li>Nações insulares na região do Pacífico podem desaparecer com a elevação do mar;</li> 
										<li>Muitas nações africanas podem sofrer secas e escassez de alimentos;</li> 
										<li>As condições de seca são prováveis no oeste dos EUA, enquanto outras áreas verão tempestades mais intensas;</li>
										<li>A Austrália provavelmente sofrerá extremos de calor e seca.</li>
									</ul>									

									<p>Na verdade, nos últimos cinco anos, acompanhamos notícias de eventos climáticos extremos – resultados direto das mudanças climáticas e que não podem ser ignorados. Abaixo, alguns exemplos:</p>
									
									<hr class="major" />

									<h2>Alguns eventos climáticos extremos e incomuns dos últimos cinco anos</h2>
									
									<ol>
										<li>50 milhões de hectares da Austrália em chamas</li>
										<p>A temporada de incêndios florestais australianos de 2019-2020 foi a definição de extremo: cerca de 50 milhões de hectares queimados, pelo menos 34 pessoas foram mortas e quase 6.000 edifícios foram destruídos.</p>
										
										<li>O verão mais quente já registrado para o hemisfério norte</li>
										<p>Em agosto de 2020, as temperaturas da superfície terrestre e oceânica combinadas de 0,94 ° C acima da média, o tornaram o segundo mês mais quente já registrado em todo o mundo desde que os registros começaram em 1880. Apenas 2016, o ano mais quente registrado no momento, teve temperaturas mais quentes em agosto.</p>
										
										<li>Cientistas dizem que o Ártico está em uma "espiral da morte"</li>
										<p>Até recentemente, os cientistas pensavam que o mais cedo que poderíamos perder todo o gelo do mar Ártico no verão seria 2050. Agora eles estão dizendo que provavelmente será 2035. Na última década, as temperaturas árticas aumentaram quase 1°C. Se as emissões de gases de efeito estufa permanecerem na trajetória em que estão agora, podemos esperar que o norte tenha se aquecido 4 °C o ano todo até a metade do século.</p>
										
										<li>Estados Unidos excluem nomes para furacões</li>
										<p>Em 2020, antes do fim da temporada de furacões nos EUA, os cientistas analisaram toda a lista alfabética de nomes de furacões e passaram para as letras gregas. Ainda mais preocupante é que os furacões de 2020 se intensificaram mais rapidamente do que o normal, resultado direto do aquecimento do clima, causando pânico entre moradores, serviços de emergência e meteorologistas.</p>										
										<li>Recorde de enchentes de versão na China</li>
										<p>Os cientistas dizem que a inundação recorde que grande parte do centro-sul da China experimentou no verão de 2020 é apenas uma prévia do que está por vir nos próximos anos. A precipitação na bacia do rio Yangtze foi a maior já registrada desde 1961, afetando mais de 55 milhões de pessoas, incluindo 3,8 milhões que foram evacuados de suas casas.</p>
										
										<li>Inundações de potencial mortal na Europa Ocidental</li>
										<p>Segundo um estudo, em um único dia de agosto de 2021, o continente europeu foi castigado por chuvas de 3 a 19% mais intensas do que seriam sem os efeitos das mudanças climáticas causadas pelo homem. O aquecimento global também aumenta entre 1,2 e 9 vezes a probabilidade de ocorrência de chuvas extremas na região, como a que provocou enchentes, também em meados de julho de 2021, na Alemanha, na Bélgica, na Holanda e em Luxemburgo. Embora seja um fenômeno até considerado comum na região, pesquisadores garantem que a mudança climática conseguiu agravar ainda mais a situação.</p>
										
										<li>Mais de 22% das zonas úmidas do Pantanal foram queimadas</li>
										<p>Cobrindo partes do Brasil, Paraguai e Bolívia, o Pantanal tem uma das maiores concentrações de biodiversidade do mundo. E em 2020, mais de 22% dele queimou, impulsionado pelas condições de seca causadas pelas mudanças climáticas. Embora algumas queimadas sejam normais a cada ano, ocorrendo naturalmente ou definidas e controladas por fazendeiros, a extensão dos incêndios de 2020 está muito fora da norma e tem muitos cientistas preocupados com seu futuro.</p>

										<li>Tailândia tem a pior seca em 40 anos</li>
										<p>A seca na Tailândia preocupou as pessoas não apenas com o sustento da população, mas também com a agricultura. Açúcar, arroz, borracha e outras safras consomem 70% do abastecimento de água do país, então, quando a água acabar, também acabará o sustento de muitas pessoas. Os especialistas preveem que a safra de açúcar deste ano poderá cair em até 30%  em relação aos anos anteriores.</p>										
										<li>Onda de calor derrete a Sibéria</li>
										<p>A Sibéria costuma ser muito fria, mas 2020 trouxe temperaturas recordes, incêndios florestais, uma perda de gelo permanente e infestações de pragas. Uma cidade registrou uma alta temperatura de 38 ° C em junho, quebrando o recorde anterior.</p>
									</ol> 

									<p>Muitos países se comprometeram a chegar à neutralidade de carbono até 2050. Isso significa reduzir as emissões de gases de efeito estufa tanto quanto possível e equilibrar as emissões restantes absorvendo uma quantidade equivalente da atmosfera, por meio de tecnologias de captura de carbono, por exemplo. A maioria dos especialistas acredita que isso é possível, mas exigirá que governos, empresas e indivíduos façam grandes mudanças.</p>
									
