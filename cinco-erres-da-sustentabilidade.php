									<span class="image main"><img src="images/cinco-erres-da-sustentabilidade.jpg" alt="Símbolos ecológicos e ambientais" /></span>

									<p>A natureza fornece matéria-prima para a fabricação de tudo que você conhece: as peças dos aparelhos eletrônicos vêm de minerais; tudo que é feito de plástico vem do petróleo; da areia é feito o vidro; o tijolo para a construção vem do barro; o cimento do calcário é retirado do solo e os móveis, das árvores. Por isso, quanto mais compramos, mais recursos naturais do planeta são retirados para sustentar o nosso consumo e mais lixo jogamos fora! Será que podemos mudar esse ciclo? O que você faria para produzir menos resíduos?</p>

									<p>Em casa, na escola e em outros ambientes que frequentamos, há sempre lixo sendo produzido. É casca de fruta, embalagem do biscoito, garrafa d’água, lata de suco, caixa de leite... muita gente joga tudo isso fora misturado, sem pensar que o lixo é de cada um e que, portanto, cada um deveria pensar bem na quantidade de lixo que produz e na forma de descartá-lo. Associado ao tema de educação ambiental e do desenvolvimento sustentável, todos os cidadãos podem colaborar com a conservação do meio ambiente seguindo a teoria dos 5Rs, a saber:</p>

									<p>Os cinco erres - ou 5Rs - integram uma proposta para a conservação do meio ambiente que tem o objetivo de nos conscientizar e nos levar a gerar menos lixo. Até pouco tempo, falávamos em três erres (ou 3Rs), que eram Reciclar, Reutilizar e Reduzir. Recentemente, outros dois erres surgiram: o R de Recusar e o R de Repensar. Quer entender melhor o que cada erre (R) significa? Sigamos!</p>

									<hr class="major" />

									<h2>Reciclar</h2>
									<p>Muitas coisas que jogamos fora podem ser reaproveitadas e transformadas em novos produtos. Por exemplo: o vidro de azeitonas, a lata da sardinha e a do refrigerante podem ser derretidos e novamente moldados, para fabricar outros potes de vidro ou outras latinhas. Outro exemplo: garrafas PET podem ser transformadas em fibras de tecido. Essa transformação de materiais usados em novos produtos é o que chamamos <b>Reciclar</b>!</p>
									
									<hr class="major" />

									<h2>Reduzir</h2>
									<p>Ter uma lixeira própria na sua casa para colocar separadamente cada tipo de lixo - como as embalagens de leite e suco, as latinhas, os plásticos, os vidros e papéis e papelão - é contribuir com a coleta seletiva (conheça mais sobre ela <a href="coleta-seletiva.php" target="_blank">clicando aqui</a>). A coleta seletiva de resíduos faz diminuir a quantidade de lixo que é despejada nos aterros sanitários. Mas podemos gerar menos lixo, se comprarmos menos ou mudarmos hábitos de consumo, reduzindo, assim, a proliferação do lixo. Ou seja: se cozinharmos a quantidade certa de alimentos para evitar o desperdício ou se usarmos por mais tempo um aparelho eletrônico que ainda funciona bem. Isso é <b>Reduzir</b>!</p>

									<hr class="major" />

									<h2>Reutilizar</h2>
									<p>Existe ainda a possibilidade de reformar e consertar o que deixamos de usar. Consertar a sua cama para passar para outra pessoa é um exemplo disso. Doar roupas, sapatos ou brinquedos para quem precisa também faz com que os objetos possam ser utilizados por mais tempo. Assim, evitamos a retirada abusiva de matéria-prima da natureza. Isso é <b>Reutilizar!</b> Podemos reutilizar materiais como sacolas de supermercado, potes de vidro e plástico, dentre outros.</p>

									<hr class="major" />

									<h2>Os outros dois erres</h2>
									<p><b>Repensar</b> é a mudança de uma atitude que estamos acostumados a praticar e realizar outra que irá diminuir o consumo e o uso de novas matérias-primas. O que poderíamos fazer de diferente? Por exemplo, em vez de comprar uma garrafinha de água todas as vezes que sentimos sede, colocamos na mochila uma garrafinha, que vamos enchendo cada vez que ela se esvazia.</p>

									<p><b>Recusar</b> é dizer não ao uso de coisas que utilizamos uma única vez e, em seguida, jogamos no lixo. Será que dá para viver sem o copinho e o canudinho de plástico? Tente imaginar a quantidade enorme desses objetos que são jogados fora todos os dias!</p>

