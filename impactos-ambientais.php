                    <p>Os impactos ambientais, são alterações no nosso ambiente que são causadas pelo desenvolvimento das atividades dos seres humanos. Nesse contexto, temos duas classificações para esse tema. Temos os impactos negativos e os positivos. Porém, geralmente o termo impactos ambientais é usado apenas para os efeitos negativos causados pela atividade humana, que são geralmente quando essas alterações oferecem riscos ao ser humano ou para os recursos naturais do planeta.</p>

                    <p>Ou seja, por conta do modelo de desenvolvimento da nossa sociedade moderna, que é baseado muito na exploração intensiva de recursos naturais do mundo, que eram vistos como fonte inesgotável de matéria-prima e de energia para a produção de diversos produtos.</p>

                    <h2>Principais impactos Ambientais</h2>

                    <p>
                        Os impactos ambientais negativos causados pelas atividades do ser humano que mais se destacam são: redução da biodiversidade de plantas e animais; contaminação do ar, água, fauna e flora; Compactação, impermeabilização, redução da fertilidade e erosão do solo; Esgotamento dos mananciais; Alterações climáticas e destruição da camada de ozônio.
                    </p>

                    <b>
                        <li>Redução da biodiversidade de plantas e animais:</li>
                    </b>


                    <p>Como explicado anteriormente, devido às atividades humanas, principalmente após a revolução industrial. Tornou muito comum a substituição da vegetação nativa pelas construções humanas. Sendo assim as vegetações dos mais diferentes biomas foram substituídas por estradas, fazendas, indústrias e cidades. Com isso, diminui cada vez mais o habitat de muitas espécies de animais e plantas. Dessa forma, muitas espécies estão sendo extintas ou correm esse risco caso sejam mantidas as formas atuais de apropriação da natureza.</p>

                    <b>
                        <li>Contaminação do ar, água, fauna e flora:</li>
                    </b>

                    <p>As atividades realizadas pelos seres humanos, geram bastante resíduos, que vão se acumular na natureza que causa a poluição e contaminação do ar, água, solo, fauna e flora.</p>

                    <b>
                        <li>Compactação, impermeabilização, redução da fertilidade e erosão do solo:</li>
                    </b>

                    <p>As atividades agropecuárias, quando executadas da maneira incorreta, sem preocupação ambiental, favorecem a compactação, a redução da fertilidade e a erosão do solo. Dificultando ou até impossibilitando o desenvolvimento de tais atividades e podendo causar danos irreversíveis ao solo. Por exemplo, as massas asfálticas usadas nas cidades e estradas, impedem a infiltração da água, ocasionando alagamentos e dificuldades no abastecimento das águas subterrâneas.</p>

                    <b>
                        <li>Esgotamento dos mananciais:</li>
                    </b>

                    <p>A maioria das atividades humanas necessitam de uma grande quantidade de água, dessa forma causando uma exploração intensiva dos recursos hídricos do planeta para abastecer indústrias, cidades e fazendas. E apesar da água ser um recurso abundante no nosso planeta, a alta demanda em conjunto com a má utilização, já tem causado escassez em locais que não sofriam com esse problema, exemplo disso é o Brasil, que apesar de ser muito rico em relação a esse recurso essencial, tem periodicamente sofrido com a falta dele em seus mananciais.</p>

                    <b>
                        <li>Alterações Climáticas:</li>
                    </b>

                    <p>O desenvolvimento da sociedade capitalista, em especial a partir da revolução industrial, tem causado grandes alterações no clima mundial. Acredita-se que por isso, aumentou o efeito estufa e aquecimento global do mundo, pois as indústrias e automóveis contribuem para a conservação do calor na atmosfera, aumentando assim a temperatura do nosso planeta.</p>

                    <b>
                        <li>Destruição da camada de ozônio:</li>
                    </b>

                    <p>Os gases lançados na atmosfera, contribuem para a destruição da camada de ozônio. Podemos falar dos clorofluorcarbono que quando entra em contato com a camada de ozônio, se desintegra e libera cloro que por sua vez reage com ozônio e compromete a camada protetora da terra. Nesse contexto, entre os tipos de gases que comprometem essa camada da terra oque tem consequências mais agressivas é o CFC113a, presente em inseticidas agrícolas. Temos também o HCFC 133a que está cada vez mais sendo alvo de estudos científicos, pois é utilizado em pequenas quantidades na fabricação de aerossóis e refrigerantes.</p>

                    <p>Dessa forma, em virtude do comprometimento da vida no nosso planeta que esses impactos podem causar. Tem crescido o debate sobre as questões ambientais e estudos para conscientizar a sociedade para um desenvolvimento mais sustentável.</p>

