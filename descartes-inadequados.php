									<p>Todos os anos no Brasil são geradas mais de 79 milhões de toneladas de lixo. Desse total, acredita-se que 30% poderia ser reciclado, mas apenas 3% de fato passa pelo processo. E como são descartados os objetos que utilizamos no dia a dia mas que não podem ir para o lixo comum? Óleo de cozinha, eletrônicos, remédios e materiais de construção, por exemplo, não devem ser descartados no mesmo lugar que embalagens e lixo orgânico. Confira os problemas causados por essa prática e como descartar esses materiais da maneira correta:</p>

									<hr class="major" />

									<h2>Descarte de lixo eletrônico</h2>
									<p>Substâncias tóxicas como mercúrio, chumbo e berílio estão presentes nos materiais eletrônicos e, se estes não forem descartados corretamente podem contaminar o ambiente, causando problemas graves de saúde pública. Para descartar o celular antigo que você não usa mais, por exemplo, procure no manual do produto ou na internet empresas que aceitam esse tipo de material. Assim, ele vai ser desmontado e as diferentes peças podem ser reutilizadas ou recicladas. Estima-se que 90% do lixo eletrônico comum possa ser reaproveitado de alguma forma.</p>
									
									<hr class="major" />

									<h2>Descarte de entulho da construção civil</h2>
									<p>O descarte incorreto de entulho pode causar enchentes e criar vetor de doenças, além de contribuir para a lotação de aterros. Mas então como descartar esses resíduos de forma adequada? O entulho deve ser reciclado ou reutilizado. Para que isso aconteça, o ideal é entrar em contato com a prefeitura e perguntar quais empresas realizam a coleta desses materiais na sua cidade.</p>

									<hr class="major" />

									<h2>Descarte de óleo</h2>
									<p>1 litro de óleo pode contaminar até 25 mil litros de água. Isso acontece porque suas substâncias não se dissolvem em água e, quando despejadas nos cursos d’ água, causam descontrole do oxigênio e a morte de peixes e outras espécies. O óleo, que dificilmente se decompõe, pode contaminar ainda o solo e, consequentemente, os lençóis freáticos. No entanto, existe uma boa solução para esse problema: a reciclagem. Você pode acumular o que sobrou em garrafas pet e levar aos postos de reciclagem que dão um destino adequado ao material. O óleo descartado corretamente é utilizado para produção de biodiesel, sabão, tintas a óleo, massa de vidraceiro e outros produtos.</p>

									<hr class="major" />

									<h2>Descarte de medicamentos</h2>
									<p>O que fazer quando a data de validade de um medicamento expira? Descartá-los no vaso sanitário ou no lixo comum não é a saída. Cada quilograma de medicamento descartado incorretamente pode acabar contaminando até 450 mil litros de água, causando um grave problema ambiental e de saúde pública. Diversas redes de farmácia contam com pontos de coleta de remédios que não serão utilizados, para que possam ter o destino adequado.</p>

