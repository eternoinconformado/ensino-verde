                    <p>A definição mais direta para o desenvolvimento sustentável é a capacidade de uma sociedade evoluir suprindo as necessidades atuais, sem comprometer a capacidade de atender as demandas futuras. Ou seja, é um um modelo de desenvolvimento que não esgota os recursos para o futuro.</p>

                    <p>Pelo fato dos recursos naturais serem finitos, é evidente a necessidade do desenvolvimento sustentável. Por isso, para ser alcançado. É necessário o reconhecimento da sua importância e planejamento.</p>

                    <p>Porém, muitas vezes o desenvolvimento é confundido com crescimento econômico, que geralmente depende do consumo crescente de energia e recursos naturais. Levando ao esgotamento dos recursos naturais aos quais a humanidade necessita. Sendo insustentável a médio e longo prazo.</p>

                    <p>Dessa forma, o desenvolvimento sustentável é uma alternativa de foco em qualidade em vez de quantidade. Dando um enfoque na redução do uso das matérias primas e produtos e o aumento da reutilização e da reciclagem.</p>

