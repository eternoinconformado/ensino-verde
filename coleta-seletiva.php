									<h2>Reciclagem e coleta seletiva</h2>

									<p>A Coleta Seletiva, hoje em dia, é uma maneira ecologicamente recomendada às populações, visto que sua função é reciclar a quantidade excessiva de material descartado pelo homem.</p>

									<p>Para tanto, há os postos de reciclagem (que dentre outras coisas recebem, por exemplo, óleo usado); bem como a separação de resíduos distintos, dispostos em lixeiras coloridas, onde cada cor significa um tipo de produto a ser descartado.</p>

									<p>Assim, o azul é destinado aos papéis e papelões; o verde aos vidros; o vermelho para os plásticos; o amarelo para os metais; o marrom para os resíduos orgânicos; o preto para madeiras; o cinza para materiais não reciclados; o branco destinado aos lixos hospitalares; o laranja para resíduos perigosos; e o roxo para resíduos radioativos.</p>

									<p>As principais formas de coleta seletiva são:</p>

									<ul>
										<li>Postos de Entrega Voluntária (PEV): Dispostos em alguns lugares estratégicos de bairros, no qual o cidadão deposita seus resíduos nos contêineres dispostos para os diversos tipos de resíduos.</li>
										<li>Postos de Troca: Aqui o cidadão leva seus resíduos e os troca por algum bem. Por exemplo, há postos em que o cidadão ganha sabão desde que leve seu óleo utilizado.</li>
										<li>Porta a Porta: Nesse modelo de coleta seletiva, os trabalhadores recolhem os resíduos nos bairros deixados pelos moradores em determinado dia da semana.</li>
										<li>Programa Interno de Coleta Seletiva (PIC): Em parceria com a associação de catadores de lixo esse programa é realizado em Instituições públicas e privadas.</li>
									</ul>

									<p>A Coleta Seletiva está intimamente relacionada à Educação Ambiental na medida em que desperta o interesse das comunidades acerca dos problemas do desperdício, do consumo, da poluição e dos prejuízos ambientais.</p>

