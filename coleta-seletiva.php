									<h2>Reciclagem</h2>

									<p>A reciclagem é uma das formas de reaproveitamento de matéria prima. Ou seja, seu objetivo é diminuir a quantidade de lixo gerado pelo homem. Essa preocupação, começou em meados da década de 70, na qual foi despertado interesse dos biólogos, ecologistas e estudiosos da área sobre os problemas causados pela poluição do meio ambiente.</p>

									<p>Esse processo de reciclagem impacta diretamente o meio ambiente, reintroduzido na cadeia produtiva itens com potencial de reaproveitamento que seriam descartados. Dando nova vida aos resíduos evitando impactos ambientais e problemas sanitários. </p>

									<p>O termo “Reciclagem” é proveniente da língua inglesa em que “re” significa repetir e “cycle” corresponde a ciclo. Logo esse termo tem o sentido de “repetir o ciclo”.</p>

									<h2>Importância da reciclagem</h2>

									<p>Além de ser um método totalmente eficiente para tratar os resíduos e recondicionar os materiais, a reciclagem também tem um grande impacto social que é de sustentar várias famílias de catadores, por exemplo, na qual muitas vezes não tem outras oportunidades e conseguem proporcionar um sustento digno às suas famílias com esse trabalho.</p>

									<h2>História da reciclagem</h2>

									<p>Dizem que foi na “Era de ouro do capitalismo”, após a segunda guerra mundial, onde tivemos uma grande alta no consumo, consequentemente um aumento dos residuos sólidos que chamaram a atenção para a redução de lixo. Então alguns países, começaram a dar um destino diferente ao material descartado. </p>
									
									<h2>Reciclagem no Brasil</h2>

									<p>O processo de reciclagem no Brasil remonta mais de 100 anos, o primeiro setor a reutilizar a materia prima foi a industria, mais especificamente na fabricação de celulose. Mas com o passar dos anos o conceito foi se expandindo pelo país e hoje é um tema que faz parte da consciencia ambiental e cidadã.</p>

									<p>É evidente, que o conceito tem muito a ser expandido no país, afinal o sistema de reciclagem ainda é pontual, ou seja, ele não ocorre na maioria dos municípios brasileiros. Entretanto alguns, oferecem o serviço de coleta seletiva a 100% das residências, algumas delas são: </p>

									<ol>
										<li>Itabira (MG)</li>
										<li>Santo André (SP)</li>
										<li>Londrina (PR)</li>
										<li>Santos (SP)</li>
										<li>Curitiba (PR)</li>
										<li>Goiânia (GO)</li>
									</ol>

									<p>Em São Paulo possui duas máquinas automatizadas que têm a capacidade de selecionar mais de 250 toneladas de materiais recicláveis por dia. As estruturas contam com esteiras, leitores óticos e outros equipamentos que separam os recicláveis por categoria (papéis, plásticos, metais, vidros e embalagens do tipo Tetra Pak), tamanho e cor. A tecnologia empregada nessas máquinas é a mesma utilizada em países referência em reciclagem, como Alemanha e França.</p>

